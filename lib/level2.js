module.exports = {
  addition: function (a, b) {
    return a + b;
  },
  substract: function (a, b) {
    return a - b;
  },
  multiplie: function (a, b) {
    return a * b;
  },
  division: function (a, b) {
    return a / b;
  },
  percentage: function (a, b) {
    return (100 * (b - a)) / a;
  },
};
